import turtle
turtle.shape('turtle')
turtle.speed(10)
def circle():
    for i in range(360):
        turtle.speed(10)
        turtle.forward(1)
        turtle.left(1)
circle()
turtle.right(180)
circle()
turtle.right(60)
circle()
turtle.right(180)
circle()
turtle.left(120)
circle()
turtle.left(180)
circle()
